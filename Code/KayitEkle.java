    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String[] header = new String[]{"Kurs Adı","Kur Bilgisi","Öğrenci Sayısı","Sınıf","Şube","ID"};
        DefaultTableModel dtm1 = new DefaultTableModel(header,0);
        jTable2.setModel(dtm1);
        kursSecimVisible();
        ArrayList<Kurs> arr = new ArrayList<Kurs>();
        Database db = new Database("DilKursu","1234");
        if( "İngilizce".compareTo((String) kursSecim.getSelectedItem()) == 0){
            arr = db.ingKurslariGetir();
        }
        else if("Almanca".compareTo((String) kursSecim.getSelectedItem()) == 0){
            arr = db.almKurslariGetir();
        }
        
        for(Kurs tmp: arr){
            Object[] objs = {tmp.getKursAdi(),tmp.getKur(),tmp.getOgrenci_sayisi(),tmp.getSinif(),tmp.getSube(),tmp.getKursId()};
            dtm1.addRow(objs);
        }
    }//GEN-LAST:event_jButton1MouseClicked

