    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Database db = new Database("DilKursu","1234");
        ogr = new Ogrenci(jTextField1.getText(),jTextField2.getText(),jTextField3.getText(),jTextField4.getText());
        if(jTextField1.getText().compareTo("") != 0 && jTextField2.getText().compareTo("") != 0 && jTextField3.getText().compareTo("") != 0 && jTextField4.getText().compareTo("") != 0 ){
            
            if(kmp.getKampanyaId() == 0 && kampanyaFlag && kursFlag){
                if(!db.ogrenci_ekle(ogr, kurs, kmp.getKampanyaId())){System.out.println("\n"+kampanyaFlag);
                    jOptionPane1.showMessageDialog(null, "Ekleme Yapılamadı");
                }
                else{
                    jOptionPane1.showMessageDialog(null, "Ekleme Yapıldı");
                }
            }
            else if(kampanyaFlag){
                if(!db.ogrenci_ekle(ogr, db.kurs_getir(Integer.parseInt(kmp.getBedava_verilen_kurs_id())), kmp.getKampanyaId())){
                    jOptionPane1.showMessageDialog(null, "Ekleme Yapılamadı");
                }
                else{
                    jOptionPane1.showMessageDialog(null, "Ekleme Yapıldı");
                }
            }else{
                jOptionPane1.showMessageDialog(null, "Ekleme Yapılamadı");
            }
        }
        else{
            jOptionPane1.showMessageDialog(null, "Ekleme Yapılamadı");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

